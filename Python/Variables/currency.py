#cambio de yen, won y yuan a dolares

yen = int(input('Cuanto te queda de yen: '))
yen = yen * 0.0069
won = int(input('Cuanto te queda de won: '))
won = won * 0.00073
yuan = int(input('Cuanto te queda de yuan: '))
yuan = yuan * 0.14

print("Tienes", yen + won + yuan, "Dolares")

